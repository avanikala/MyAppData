class Book{
private int bookId;
private String name;
private String author;

public void setbookId(){
this.setbookId = bookId;
}
public int getbookId(){
	return bookId;
}
public void setName(){
this.setName = name;
}
public int getName(){
	return name;
}
public void setAuthor(){
this.setAuthor= author;
}
public int getAuthor(){
	return author;
}
}